    /***
     * check vuforia and return a number that can probably be used to find out which position the skystone is in
     * @param debug will print telemetry if true, won't if false
     * @return sum of x + y
     */
    public double getData(boolean debug) {
        // check all the trackable targets to see which one (if any) is visible.
        targetVisible = false;
        for (VuforiaTrackable trackable : allTrackables) {
            if (((VuforiaTrackableDefaultListener) trackable.getListener()).isVisible()) {
//                if (debug)
                opmode.telemetry.addData("Visible Target", trackable.getName());
                targetVisible = true;

                // getUpdatedRobotLocation() will return null if no new information is available since
                // the last time that call was made, or if the trackable is not currently visible.
                OpenGLMatrix robotLocationTransform = ((VuforiaTrackableDefaultListener) trackable.getListener()).getUpdatedRobotLocation();
                if (robotLocationTransform != null) {
                    lastLocation = robotLocationTransform;
                }
                break;
            }
        }

        // Provide feedback as to where the robot is located (if we know).
        if (targetVisible) {
            VectorF translation = lastLocation.getTranslation();
            // express position (translation) of robot in inches.
            if (debug)
                opmode.telemetry.addData("Pos (in)", "{X, Y, Z} = %.1f, %.1f, %.1f",
                        translation.get(0) / mmPerInch, translation.get(1) / mmPerInch, translation.get(2) / mmPerInch);
            // express the rotation of the robot in degrees.
            Orientation rotation = Orientation.getOrientation(lastLocation, EXTRINSIC, XYZ, DEGREES);
            if (debug)
                opmode.telemetry.addData("Rot (deg)", "{Roll, Pitch, Heading} = %.0f, %.0f, %.0f", rotation.firstAngle, rotation.secondAngle, rotation.thirdAngle);
            return translation.get(0) / mmPerInch + translation.get(1) / mmPerInch;
        } else {
            if (debug)
                opmode.telemetry.addData("Visible Target", "none");
            return Double.NaN;
        }


//        }

    }

    ElapsedTime vuforiaTimeout = new ElapsedTime();

    /***
     * you'll have to get numbers from the getData function and input them here, it will compare values to return what one it thinks it is based off of the numbers
     * @param left
     * @param center
     * @param right
     * @return {Left, Right, Center, Unknown}
     */
    public Position getPosition(double left, double center, double right) {
        vuforiaTimeout.reset();
        while (vuforiaTimeout.seconds() < 5) {
            double data = getData(false);
            if (!Double.isNaN(data)) {
                double val = Math.abs(left - data);
                Position output = Position.Left;
                if (Math.abs(center - data) < val) {
                    val = Math.abs(center - data);
                    output = Position.Center;
                }
                if (Math.abs(right - data) < val) {
                    output = Position.Right;
                }
                return output;
            }
        }
        return Position.Unknown;
    }

